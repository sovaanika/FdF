#include "fdf.h"

void	draw(t_fdf *fdf)
{
	int		x;
	int		y;

	x = -1;
	while (++x < WIDTH)
	{
		y = -1;
		while (++y < HEIGTH)
		{
			fdf->data[y * WIDTH + x] = 0;
		}
	}
	celldraw(fdf, 50, 50);
}